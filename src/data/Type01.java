package data;

public class Type01 {
	
	public static void main(String[] args) {
		//문자
		char c = 'a';
		
		//숫자-정수
		byte b = 127;
		b = -128;
		
		short sh = 123;
		
		int i = 123123123;
		
		long l = 10000000000l;
		
		//숫자 - 실수
		float f = 1.2f; //기본 실수형은 double
		double db = 1.2;
		
		//논리형(불린)
		boolean b1 = true;
		
	}

}